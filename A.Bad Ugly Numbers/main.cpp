#include <bits/stdc++.h>

using namespace std;

int main()
{
    int k;
    int n;
    scanf("%d",&k);
    while(k--){

        scanf("%d",&n);
        if(n==1){
            printf("-1\n");

        }
        else{
             n--;
             while(n--)
                printf("3");
             printf("4\n");

    }

    }
    return 0;
}
